# ============ 维度建议Prompt ============
DIMENSION_SUGGEST_PROMPT = """
## 任务
你是一个专业的招聘顾问。根据提供的岗位信息，为该岗位生成评估维度列表，并为每个维度生成专属的简历评估提示词。

## 输入信息
- 岗位名称: {job_name}
- 岗位描述: {job_description}

## 输出要求
生成3-6个评估维度，每个维度包含：
1. dimension_name: 维度名称（10字以内，如"技术深度"、"项目经验"、"学历背景"）
2. weight: 权重（所有维度权重之和必须精确等于1.0，使用两位小数）
3. prompt_template: 专为该维度定制的简历评估提示词模板，必须包含 {{resume_text}} 和 {{job_name}} 占位符

### prompt_template格式规范
每个维度的prompt_template需包含：
- 明确的维度说明和评分标准
- 针对该维度的具体评分要素
- 输出格式：{{"score": <0-100整数>, "advantage": "<优点30-100字>", "disadvantage": "<缺点30-100字，无则为空字符串''>"}}

### 输出格式（严格JSON数组）
[
  {{
    "dimension_name": "维度名称",
    "weight": 0.35,
    "prompt_template": "完整的提示词模板文本，包含{{{{resume_text}}}}和{{{{job_name}}}}占位符"
  }}
]

## 生成规则
1. 权重分配要合理，核心维度权重应更高
2. 维度覆盖全面：技术能力、经验年限、学历、软技能等视岗位灵活组合
3. prompt_template必须专门针对该维度，不得使用通用模板
4. 所有权重之和必须精确为1.0
"""

