# ============ 技能建议Prompt ============
SKILL_SUGGEST_PROMPT = """
## 任务
你是一个专业的招聘顾问。根据提供的岗位信息，为该岗位生成技能要求列表。

## 输入信息
- 岗位名称: {job_name}
- 岗位描述: {job_description}

## 输出要求
生成8-15个技能要求，分为三个优先级：

### 技能类型定义
- type=1 (必须满足): 胜任该岗位的核心技能，缺失则直接淘汰
- type=2 (优先匹配): 显著提升竞争力的技能，命中则标记为优秀/良好
- type=3 (普通技能): 加分项，命中可提升分数

### 输出格式（严格JSON数组）
[
  {{"skill": "技能名称", "type": 1|2|3, "reason": "该技能对岗位的重要程度说明，10-20字"}}
]

## 生成规则
1. 技能名称必须具体明确，不得使用模糊描述
2. 必须包含该岗位最核心的1-2个框架/语言
3. 必须包含2-3个通用能力（如协作、沟通）
4. 优先选择市场中该岗位最主流的技术栈
5. reason必须说明该技能在岗位中的实际应用场景
"""

# ============ 维度评估Prompt ============
DIMENSION_EVAL_PROMPT = """
## 任务
你是一个专业的简历评估专家。请根据以下维度对候选人的简历进行评估。

## 输入信息
- 评估维度: {dimension_name}
- 岗位名称: {job_name}
- 岗位技能要求: {job_skills}

## 简历内容
{resume_text}

## 评分标准
| 分数区间 | 等级 | 说明 |
|----------|------|------|
| 90-100 | 优秀 | 显著超出岗位预期，在该维度有突出表现 |
| 70-89 | 良好 | 符合岗位预期，能够胜任该维度工作 |
| 50-69 | 一般 | 基本符合，但存在明显不足 |
| 0-49 | 未达标 | 明显缺失，无法胜任该维度工作 |

## 输出要求（严格JSON格式，不得添加任何额外内容）
{{"score": <整数 0-100>, "advantage": "<优点描述30-100字，基于简历内容，无优点写'无'>", "disadvantage": "<缺点描述30-100字，基于简历与岗位差距，无缺点精确返回空字符串''>"}}

## 评分原则
1. 基于简历内容：优点必须来自简历原文，不得无中生有
2. 对照岗位需求：缺点必须明确指出与岗位要求的差距
3. 具体化描述：避免"不错"、"还可以"等模糊表述
4. 量化优先：优先引用具体数据（项目规模、技术难度等）
"""

# ============ 综合评价Prompt ============
COMPREHENSIVE_EVAL_PROMPT = """
## 任务
你是一个专业的招聘顾问。请生成简历对该岗位的综合评价。

## 输入信息
- 岗位名称: {job_name}
- 最终匹配得分: {final_score}/100
- 各维度评估结果: {dimensions}

## 输出要求（严格JSON格式）
{{"advantage_comment": "<优点总结50-150字，基于各维度优点提炼>", "disadvantage_comment": "<缺点总结50-150字，无明显缺点精确返回空字符串''>"}}

## 评价规则
1. 优点从各维度优势中提炼3-5个核心亮点
2. 缺点从各维度不足中提炼主要差距，若各维度均无明显缺点则精确返回空字符串''
3. 综合评价必须与各维度得分一致
4. 优点引用简历中的具体经历，缺点以改进方向表述
"""

